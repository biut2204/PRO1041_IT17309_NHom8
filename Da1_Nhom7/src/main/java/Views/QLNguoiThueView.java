    private void tb_bangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_bangMouseClicked
        // TODO add your handling code here:
        int index = tb_bang.getSelectedRow();
        NguoiThue nt = ntR.getAllData().get(index);
        txt_hoten2.setText(String.valueOf(nt.getHoTen()));
        txt_ngaysinh2.setText(String.valueOf(nt.getNgaySinh()));
        txt_sdt2.setText(String.valueOf(nt.getSdt()));
        txt_diachi2.setText(String.valueOf(nt.getDiaChi()));
        cbb_dixe2.setSelectedItem(nt.getDiXe());
        txt_trangthai.setText(nt.getTrangThai());
        if (nt.getGioiTinh().equals("Nam")) {
            rd_nam2.setSelected(true);
        } else {
            rd_nu2.setSelected(true);
        }
    }//GEN-LAST:event_tb_bangMouseClicked

